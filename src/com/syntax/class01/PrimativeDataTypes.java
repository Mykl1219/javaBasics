package com.syntax.class01;

public class PrimativeDataTypes {

	public static void main(String[] args) {
		
		    // Whole numbers
		
 //   Data Type	 box(any name)  value
		byte       box1       =   10;
		short      box2       =   20;
		int        box3       =   30;
		long       box4       =   40l; // Must add l or L at end of value
		
		   // Decimal Numbers
		
//	  Data Type  box(any name)    value
		float      price1     =   12.99f; // Must add f or F at end of value
		double     price2     =   13.99;
		
		   // Single Character
		
//    Data Type  box(any name)    value		
		char       gender     =    'F';
		char       sign       =    '$';
		
	// Boolean (Yes or No, True or False)
		
//	   Data Type  box(any name)    value
		boolean    snow       =    false;
		boolean    tired      =    true;
		
		// Print out 13.99
		System.out.println(price2);
		System.out.println(sign);

	}

}
