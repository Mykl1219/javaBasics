package com.syntax.replit;

public class ArithmaticOperators011 {

	public static void main(String[] args) {
		
		/*
		 * Write a program to print the perimeter and area of a rectangle with width = 5 and height = 8.
		 */
		
		int wid=5;
	    int hgt=8;

	    int per=2*(wid+hgt);
	    int area=wid*hgt;

	    System.out.println("The perimeter of a rectangle with width "
	    +wid+" and height "+hgt+" is equals to "
	    +per+" and the area equals to "+area);

	}

}
