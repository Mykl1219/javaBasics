package com.syntax.class02;

public class CreatingVariables {

	public static void main(String[] args) {
		
// Declaration  Initialize
	int age   =     25;
	
// Same as above use below for multiple variables
	int num; // Declare variable
	num=10; //  Initialize
	num=100; // Reassign variable
	
	int n1,n2,n3;
	n1=10;
	n2=20;
	n3=30;
	
// Reassigning the original variable of age
	age=26;
	age=75;
	
	
	System.out.println(age);

	}

}
